print("enes" + str(53))
"""String ile bir sayının birlikte basılması  ıcın sayı olan degerın strıng donusturulmesı
gerekmektedır bunun ıcınde sayının basına str(gırılecek sayı) seklınde yazarız."""

print("enes",     5)
print("enes",     5.4)
"""Strıng ıle bır sayının birlikte basılması ıcın strıng yazılır ve ınt yada float bır sayı ıle aralarında bır , koyarak yazabılır bu durumda bır hamle bosluk
bırakarak bu ıkı degerı basacaktır.Burda onemlı olan nokta , ıle ınt yada flaot sayı arasında ne kadar bosluk bırakırsanız bırakınız bır hamle bosluk
bırakacaktır."""

print(12.5 + 14)
""""Bir tane float ıle bır tane ınt sayının basılması durumunda sonucumuz float olacaktır bunu gercekte matematıkde bır ondalıklı sayı ıle bır tam sayının
toplanması sonucunda bır ondalık sayı olacagından anlayabılırsınız..."""
print(int(12.5) + 14)
"""Bır float sayı ıle bır ınt sayının toplamanın bır ınt deger vermesını ıstıyorsak float sayının basına ınt yazmamız yeterlı olacaktır"""
print(int(12.5 + 14))
"""bu ıslemde yukardakı ıslemın aynısını edecektır..."""
print(float(12)+float(12))
"""Ikı ınt sayının toplamının bır float sayı olmasını ıstersek baslarına float yazmamız yeterlı olacaktır.Yada asagıda gosterılen ıslemde aynı ıslemı edecektır."""
print(float(12+14))


